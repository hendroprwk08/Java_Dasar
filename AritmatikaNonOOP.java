class AritmatikaNonOOP{
    public static  void main(String[]args)
    {
        int a=10,b=2;
        
        System.out.println("operasi kali "+ (a*b));
        System.out.println("operasi Bagi "+ (a/b));
        System.out.println("operasi tambah "+ (a+b));
        System.out.println("operasi kurang "+ (a-b));
        System.out.println("operasi modulus (sisa bagi) "+ (a%b));
    }
}